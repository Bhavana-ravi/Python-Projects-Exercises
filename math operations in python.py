# Basic Math operations in python(1)
# Addition, Subtraction, Multiplication, Division, Power, Modulus

books = 5

# Addition
books += 1
print(f"Addition: {books}")

# Subtraction
books -= 2
print(f"Subtraction: {books}")

# Multiplication
books *= 3
print(f"Multiplication: {books}")

# Division
books /= 5
print(f"Division: {books}")

# Power
books **= 4
print(f"Power: {books}")

#Modulus
remainder = books % 2
print(f"Modulus: {remainder}")
